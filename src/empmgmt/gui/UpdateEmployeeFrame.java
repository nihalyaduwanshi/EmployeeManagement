    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        EmpPojo emp;
        String str=txtEmpNo.getText().trim();
        if(str.length()==0)
        {
            JOptionPane.showMessageDialog(null, "Please Enter Emp Number first");
            txtEmpNo.setText("");
            txtenameold.setText("");
            txtsalnew.setText("");
            txtenamenew.setText("");
            txtsalnew.setText("");
            return;
        }
        try{
            int eno=Integer.parseInt(str);
            String ename=txtenamenew.getText().trim();
            double esal=Double.parseDouble(txtsalnew.getText().trim());
            if(!ename.isEmpty() && esal>0)
            {
                emp=new EmpPojo(eno,ename,esal);
                EmpDao.updateEmp(emp);
                JOptionPane.showMessageDialog(null, "Record Updated successfully","success!",JOptionPane.INFORMATION_MESSAGE);

            }
            else
            JOptionPane.showMessageDialog(null, "Please enter valid Name","error!",JOptionPane.ERROR_MESSAGE);
            
            txtEmpNo.setText("");
            txtenameold.setText("");
            txtsalnew.setText("");
            txtenamenew.setText("");
            txtsalold.setText("");
                

        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid input","error!",JOptionPane.ERROR_MESSAGE);
            txtEmpNo.setText("");
            txtenameold.setText("");
            txtsalnew.setText("");
            txtenamenew.setText("");
            txtsalold.setText("");
        } 
        catch (SQLException ex) {
             
           JOptionPane.showMessageDialog(null, "Problem in Database","error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
           System.exit(0);
        }

    }//GEN-LAST:event_btnUpdateActionPerformed

