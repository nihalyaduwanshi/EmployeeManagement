    private void btnGetDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetDetailActionPerformed
        
        String str=txtEmpNo.getText().trim();
        if(str.length()==0)
        {
            JOptionPane.showMessageDialog(null, "Please Enter Emp Number first");
            return;
        }
        try
        {
            int eno=Integer.parseInt(str);
            EmpPojo emp=EmpDao.findEmpByEmpNo(eno);
            if(emp==null)
            {
                JOptionPane.showMessageDialog(null, "Employee not found","Message!",JOptionPane.ERROR_MESSAGE);
                txtEmpNo.setText("");         
                txtenameold.setText("");
                txtsalold.setText("");
                return;
            }
            txtenameold.setText(emp.getEmpName());
            txtsalold.setText(emp.getEmpSal()+"");

        }
        catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "Please enter valid input","error!",JOptionPane.ERROR_MESSAGE);
            txtEmpNo.setText("");
        } 
        catch (SQLException ex) {
             
           JOptionPane.showMessageDialog(null, "Problem in Database","error!",JOptionPane.ERROR_MESSAGE);
           ex.printStackTrace();
           System.exit(0);
        }

    }//GEN-LAST:event_btnGetDetailActionPerformed

